namespace ConsoleApp1;

public class Calculator
{
    public int CalculateSum (int left, int right)
    {
        return left + right;
    }
    
    public int CalculateDiff (int left, int right)
    {
        return left - right;
    }

    public int CalculateMultiply (int left, int right)
    {
        return left * right;
    }

    public int CalculateDevide(int left, int right)
    {
        return left / right;
    }
}